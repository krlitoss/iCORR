    public void consultamodifica(String clavemodifica){
        if(clavemodifica.equals("")){
            /**si la clave esta vacia significa que es un registro nuevo*/
        }else{
            rs0=null;
            try{
                String senSQL="SELECT * FROM agentes WHERE id_agente='"+clavemodifica+"'";
                rs0=conexion.consulta(senSQL,connj);
                if(rs0.next()){
                    id.setText(rs0.getString("id_agente"));
                    nombre.setText(rs0.getString("nombre"));
                    comision.setText(fijo2decimales.format(rs0.getDouble("comision")));
                    email.setText(rs0.getString("email"));
                    telefono.setText(rs0.getString("telefono"));
                    metas.setText(rs0.getString("metas"));
                    comercializador.setSelectedItem(rs0.getString("comercializador"));
                    clavecliente.setText(rs0.getString("id_clientes"));
                }
                if(rs0!=null) {    rs0.close();    }
            } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }
        }
    }

