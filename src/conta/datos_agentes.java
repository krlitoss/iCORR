    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        String comer=(String)comercializador.getSelectedItem();
        if(nombre.getText().equals("")||comision.getText().equals("")||metas.getText().equals("")){
            JOptionPane.showMessageDialog(this, "VERIFICA HAY CAMPOS VACIOS", "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
        }else{
            if(comer.equals("Si") &&clavecliente.getText().equals("")){
                JOptionPane.showMessageDialog(this, "ES COMERCIALIZADOR DEBES COLOCAR CLAVE CLIENTE", "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
            }else{
                String senSQL="UPDATE agentes set nombre='"+nombre.getText()+"',comision='"+comision.getText()+"',telefono='"+telefono.getText()+"',email='"+email.getText()+"',metas='"+metas.getText()+"',comercializador='"+(String)comercializador.getSelectedItem()+"',id_clientes='"+clavecliente.getText()+"' WHERE id_agente='"+id.getText()+"'";
                if(id.getText().equals("")){
                    senSQL="INSERT INTO agentes (nombre,comision,telefono,email,metas,comercializador,id_clientes) VALUES('"+nombre.getText()+"','"+comision.getText()+"','"+telefono.getText()+"','"+email.getText()+"','"+metas.getText()+"','"+(String)comercializador.getSelectedItem()+"','"+clavecliente.getText()+"');";
                }
                conexion.modifica_p(senSQL,connj,valor_privilegio);
                salir();
            }
        }
}//GEN-LAST:event_btnaceptarActionPerformed

