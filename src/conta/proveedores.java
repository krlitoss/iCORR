    public void datos(){
        rs0=null;
        try{
            String senSQL="SELECT * FROM proveedores ORDER BY id_proveedor";
            rs0=conexion.consulta(senSQL,conn);
            while(rs0.next()){
                Object datos[]={rs0.getString("id_proveedor"),rs0.getString("nombre"),rs0.getString("rfc"),rs0.getInt("dias"),rs0.getString("contacto"),rs0.getString("telefono1"),rs0.getString("email")};
                modelot1.addRow(datos);
            }
            if(rs0!=null) {
                rs0.close();
            }
        } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }

    }
