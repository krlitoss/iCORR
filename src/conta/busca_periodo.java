    private void configuracion() {
        Date FI=new Date();
        Date FF=new Date();
        String ffi=""+conf.getProperty("FechaIni");
        String fff=""+conf.getProperty("FechaFin");
        try{
            if(ffi.equals("null") || ffi.equals("")){

            }else{
                FI=fechainsertar.parse(ffi);
            }
            if(fff.equals("null") || fff.equals("")){

            }else{
                FF=fechainsertar.parse(fff);
            }

        }catch(Exception e){
           JOptionPane.showMessageDialog(this, "ERROR EN LAS FECHAS"+e, "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
        }
        fechaini.setDate(FI);
        fechafin.setDate(FF);
        mes.setMonth(Integer.parseInt(fechames.format(FI))-1);
        ano.setYear(Integer.parseInt(fechaano.format(FI)));
    }


