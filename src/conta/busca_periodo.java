    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        mesfin=mes.getMonth();
        anofin=ano.getYear();
        estado="aceptar";

        Calendar calFin = Calendar.getInstance();
        calFin.set(anofin, mesfin, 1);
        Date fechaIni=calFin.getTime();
        calFin.set(anofin,mesfin, calFin.getActualMaximum(Calendar.DAY_OF_MONTH));
        Date fechaFin = calFin.getTime();
        
        if(r.equals("")){
            conexion.escribir("Periodo",fechamesano.format(fechaFin).toUpperCase());
            conexion.escribir("FechaIni",fechainsertar.format(fechaIni));
            conexion.escribir("FechaFin",fechainsertar.format(fechaFin));
        }

        salir();
}//GEN-LAST:event_btnaceptarActionPerformed

