                senSQL="INSERT INTO transportistas(clave_transportista, estatus, empresa, nombre, direccion, tipounidad, placas, capacidad, clave_unidad_medida,celular,nextel) VALUES ('"+clave.getText()+"', 'Act', '"+empresa.getText()+"', '"+nombre.getText()+"', '"+direccion.getText()+"', '"+tipounidad.getText()+"', '"+placas.getText()+"', '"+capacidad.getText()+"', '"+(String)unidadmedida.getSelectedItem()+"', '"+celular.getText()+"', '"+nextel.getText()+"');";
