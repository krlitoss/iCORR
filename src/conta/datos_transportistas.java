    public void consultamodifica(String clavemodifica){
        if(clavemodifica.equals("")){
            /**si la clave esta vacia significa que es un registro nuevo*/
        }else{
            clave.setEditable(false);
            clave.setFocusable(false);
            rs0=null;
            try{
                String senSQL="SELECT * FROM transportistas WHERE (estatus='Act' AND clave_transportista='"+clavemodifica+"');";
                rs0=conexion.consulta(senSQL,connj);
                if(rs0.next()){
                    clave.setText(rs0.getString("clave_transportista"));
                    empresa.setText(rs0.getString("empresa"));
                    nombre.setText(rs0.getString("nombre"));
                    direccion.setText(rs0.getString("direccion"));
                    tipounidad.setText(rs0.getString("tipounidad"));
                    placas.setText(rs0.getString("placas"));
                    capacidad.setText(""+rs0.getInt("capacidad"));
                    unidadmedida.setSelectedItem(rs0.getString("clave_unidad_medida"));
                    celular.setText(rs0.getString("celular"));
                    nextel.setText(rs0.getString("nextel"));

                }
                if(rs0!=null) {
                    rs0.close();
                }
            } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }

        }
    }
    public void unidadmedida(){
        rs0=null;
        try{
            String senSQL="SELECT * FROM unidad_medida";
            rs0=conexion.consulta(senSQL,connj);
            while(rs0.next()){
                unidadmedida.addItem(rs0.getString("clave_unidad_medida"));

            }
            if(rs0!=null) {
                rs0.close();
            }
        } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }
    }

