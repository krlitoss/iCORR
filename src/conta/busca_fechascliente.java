    private void configuracion() {
        Date FI=new Date();
        Date FF=new Date();
        String ffi=""+conf.getProperty("FechaIni")+" 00:00:00";
        String fff=""+conf.getProperty("FechaFin")+" 23:59:59";
        try{
            if(ffi.equals("null") || ffi.equals("")){

            }else{
                FI=fechainsertarhora.parse(ffi);
            }
            if(fff.equals("null") || fff.equals("")){

            }else{
                FF=fechainsertarhora.parse(fff);
            }

        }catch(Exception e){
           JOptionPane.showMessageDialog(this, "ERROR EN LAS FECHAS"+e, "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
        }
        fechaini.setDate(FI);
        fechafin.setDate(FF);
    }
