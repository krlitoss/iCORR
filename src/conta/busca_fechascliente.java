    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        try{
            if(fechaini.getDate().after(fechafin.getDate()) || fechaini.getDate()==null || fechafin.getDate()==null){
                JOptionPane.showMessageDialog(this, "FECHAS INCORRECTAS", "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
            }else{
                /**funcion para comprobar que selecciones un proveedor*/
                String seleccionaproveedor=(String)cliente.getSelectedItem();
                if(seleccionaproveedor.equals("Buscar")&&id_cli.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "TIENES QUE SELECCIONAR UN CLIENTE O CAMBIA A TODOS", "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
                }else{
                    configuracionSalida();
                    estado="aceptar";
                    salir();
                }

            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "FECHAS INCORRECTAS  "+e, "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
        }
        
}//GEN-LAST:event_btnaceptarActionPerformed

