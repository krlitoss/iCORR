    public void consultamodifica(String clavemodifica){
        if(clavemodifica.equals("")){
            /**si la clave esta vacia significa que es un registro nuevo*/
        }else{
            clave.setEditable(false);
            clave.setFocusable(false);
            descripcion.requestFocus();
            rs0=null;
            try{
                String senSQL="SELECT * FROM maquinas WHERE id_maquina='"+clavemodifica+"'";
                rs0=conexion.consulta(senSQL,connj);
                if(rs0.next()){
                    id.setText(rs0.getString("id_maquina"));
                    clave.setText(rs0.getString("clave"));
                    descripcion.setText(rs0.getString("descripcion"));
                    capacidad.setText(""+rs0.getInt("capacidad"));
                    turnos.setText(fijo1decimales.format(rs0.getDouble("turnos")));
                    costokg.setText(fijo2decimales.format(rs0.getDouble("costokg")));
                    costohr.setText(fijo2decimales.format(rs0.getDouble("costohr")));
                    arreglo.setText(""+rs0.getInt("arreglo"));
                    velocidad.setText(""+rs0.getInt("velocidad"));
                    horainicial.setText(rs0.getString("hora_inicial"));
                    unidadvel.setSelectedItem(rs0.getString("unidadvelocidad"));
                    unidadcap.setSelectedItem(rs0.getString("unidadcapacidad"));
                    notas.setText(""+rs0.getString("notas"));
                    lbimagen.setIcon(new ImageIcon(conf.getProperty("Rutaplanos")+"/"+clave.getText()+".jpg"));
                    //lbimagen.setIcon(new ImageIcon(getClass().getResource("/imagenes/"+clave.getText()+".jpg")));
                }
                if(rs0!=null) {
                    rs0.close();
                }
            } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }

        }
    }

