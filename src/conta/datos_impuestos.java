    public void consultamodifica(String clavemodifica){
        if(clavemodifica.equals("")){
            /**si la clave esta vacia significa que es un registro nuevo*/
        }else{
            rs0=null;
            try{
                String senSQL="SELECT * FROM impuestos WHERE id_impuestos='"+clavemodifica+"'";
                rs0=conexion.consulta(senSQL,connj);
                if(rs0.next()){
                    id.setText(rs0.getString("id_impuestos"));
                    leyenda.setText(rs0.getString("esquema"));
                    iva.setText(fijo2decimales.format(rs0.getDouble("iva")));
                    ieps.setText(""+rs0.getDouble("ieps"));
                    ivar.setText(""+rs0.getDouble("ivaretenido"));
                    isrr.setText(""+rs0.getDouble("isrretenido"));
                    defaul.setSelectedItem(rs0.getString("defaul"));
                }
                if(rs0!=null) {
                    rs0.close();
                }
            } catch (Exception ex) {JOptionPane.showMessageDialog(this,"ERROR AL EJECUTAR LA CONSULTA\n"+ ex,"E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);      }

        }
    }

