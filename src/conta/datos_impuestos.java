    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        if(leyenda.getText().equals("")||iva.getText().equals("")){
            JOptionPane.showMessageDialog(this, "VERIFICA HAY CAMPOS VACIOS", "E R R O R !!!!!!!!!!",JOptionPane.ERROR_MESSAGE);
        }else{
            String defaull=(String)defaul.getSelectedItem();
            if(defaull.equals("Si")){
                if(id.getText().equals("")){
                    defaul.setSelectedItem("No");
                }else{
                    String senSQL2="UPDATE impuestos set defaul='No' WHERE id_impuestos<>'"+id.getText()+"'";
                    conexion.modificamov_p(senSQL2,connj,valor_privilegio);
                }
            }

            String senSQL="UPDATE impuestos set esquema='"+leyenda.getText()+"',iva='"+iva.getText()+"',ieps='"+ieps.getText()+"',ivaretenido='"+ivar.getText()+"',isrretenido='"+isrr.getText()+"',defaul='"+(String)defaul.getSelectedItem()+"' WHERE id_impuestos='"+id.getText()+"'";
            if(id.getText().equals("")){
                senSQL="INSERT INTO impuestos (esquema,iva,ieps,ivaretenido,isrretenido,defaul) VALUES('"+leyenda.getText()+"','"+iva.getText()+"','"+ieps.getText()+"','"+ivar.getText()+"','"+isrr.getText()+"','"+(String)defaul.getSelectedItem()+"');";
            }
            conexion.modifica_p(senSQL,connj,valor_privilegio);
            
            salir();
        }
}//GEN-LAST:event_btnaceptarActionPerformed

